/**************************************************************************
* TOVALUE
**************************************************************************/
void G__bc_inst::TOVALUE(G__value *pbuf) {
#ifdef G__ASM_DBG
  if(G__asm_dbg) G__fprinterr(G__serr,"%3x: TOVALUE\n",G__asm_cp);
#endif
  G__asm_inst[G__asm_cp]=G__TOVALUE;
  typedef void (*dereff_t)(G__value*);
  switch(G__get_reftype(G__value_typenum(*pbuf))) {
  case G__PARANORMAL:
  case G__PARAREFERENCE:
    switch(G__get_type(G__value_typenum(*pbuf))) {
    case 'B': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<unsigned char>; break;
    case 'C': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<char>; break;
    case 'R': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<unsigned short>; break;
    case 'S': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<short>; break;
    case 'H': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<unsigned int>; break;
    case 'I': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<int>; break;
    case 'K': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<unsigned long>; break;
    case 'L': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<long>; break;
    case 'F': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<float>; break;
    case 'D': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<double>; break;
    case 'U': G__asm_inst[G__asm_cp+1]=(long)(dereff_t)G__asm_deref<int>; break;
    default: /* SHOULD NEVER HAPPEN */ break;
    }
    break;
  case G__PARAP2P:
    G__asm_inst[G__asm_cp+1]=(long)G__asm_tovalue_p2p;
    break;
  default:
    G__asm_inst[G__asm_cp+1]=(long)G__asm_tovalue_p2p;
    break;
  }
  inc_cp_asm(2,0);
}

